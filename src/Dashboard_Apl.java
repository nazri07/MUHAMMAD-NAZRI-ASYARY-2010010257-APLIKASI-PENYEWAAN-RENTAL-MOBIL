    private void jButton_submit1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_submit1ActionPerformed
        // TODO add your handling code here:
        try {
            Connection conn = koneksi.getConnection();
            PreparedStatement stmt;
            stmt = conn.prepareStatement("insert into pengembalian(no, nik, nama, umur, jenis_mobil, status) values(?,?,?,?,?,?)");
            stmt.setString(1, jTextField_no1.getText());
            stmt.setString(2, jTextField_nik1.getText());
            stmt.setString(3, jTextField_nama1.getText());
            stmt.setString(4, jTextField_umur1.getText());
            stmt.setString(5, jTextField_jenis_mobil1.getText());
            stmt.setString(6, jTextField_status.getText());
            stmt.executeUpdate();
            JOptionPane.showMessageDialog(null, "Data berhasil disimpan", "Pesan", JOptionPane.INFORMATION_MESSAGE);
            Pengembalian();
        } catch (SQLException e) {
            System.out.println(e.getMessage());
        }
    }//GEN-LAST:event_jButton_submit1ActionPerformed

    private void jButton_edit1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_edit1ActionPerformed
        // TODO add your handling code here:
        try {
            Connection conn = koneksi.getConnection();
            PreparedStatement stmt = conn.prepareStatement("update pengembalian set nik=?, nama=?, umur=?, jenis_mobil=?, status=? where no=?");
            stmt.setString(1, jTextField_nik1.getText());
            stmt.setString(2, jTextField_nama1.getText());
            stmt.setString(3, jTextField_umur1.getText());
            stmt.setString(4, jTextField_jenis_mobil1.getText());
            stmt.setString(5, jTextField_status.getText());
            stmt.setString(6, jTextField_no1.getText());
            stmt.executeUpdate();
            JOptionPane.showMessageDialog(null, "Data berhasil diubah", "Pesan", JOptionPane.INFORMATION_MESSAGE);
            Pengembalian();
        } catch (SQLException e) {
            System.out.println(e.getMessage());
        }
    }//GEN-LAST:event_jButton_edit1ActionPerformed

