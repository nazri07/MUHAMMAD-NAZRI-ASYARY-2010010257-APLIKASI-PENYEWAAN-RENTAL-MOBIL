    public void Peminjam() {
        DftTblModel_peminjam = new DefaultTableModel();
        DftTblModel_peminjam.addColumn("NO");
        DftTblModel_peminjam.addColumn("NIK");
        DftTblModel_peminjam.addColumn("NAMA");
        DftTblModel_peminjam.addColumn("UMUR");
        DftTblModel_peminjam.addColumn("JENIS MOBIL");
        DftTblModel_peminjam.addColumn("LAMA PENGEMBALIAN");
        jTable_peminjam.setModel(DftTblModel_peminjam);
        Connection conn = koneksi.getConnection();
        try {
            java.sql.Statement stmt = conn.createStatement();
            String SQL = "select * from peminjam";
            java.sql.ResultSet res = stmt.executeQuery(SQL);
            while (res.next()) {
                DftTblModel_peminjam.addRow(new Object[]{
                    res.getString("no"),
                    res.getString("nik"),
                    res.getString("nama"),
                    res.getString("umur"),
                    res.getString("jenis_mobil"),
                    res.getString("lama_meminjam")
                });
            }
        } catch (SQLException e) {
            System.out.println(e.getMessage());
        }
    }
    
